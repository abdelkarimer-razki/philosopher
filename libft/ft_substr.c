#include"libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*str;

	if (!s)
		return (NULL);
	i = ft_strlen(s);
	if (start < i)
	{
		if ((i - start) < len)
			str = malloc(i - start + 1);
		else
			str = malloc(len + 1);
		if (!str)
			return (NULL);
		i = 0;
		while (i < len && s[start])
			str[i++] = s[start++];
		str[i] = 0;
		return (str);
	}
	str = malloc(1);
	str[0] = 0;
	return (str);
}
