#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	k;

	if (s)
	{
		i = ft_strlen(s);
		k = 0;
		while (k < i)
		{
			write(fd, &s[k], 1);
			k++;
		}
	}
}
