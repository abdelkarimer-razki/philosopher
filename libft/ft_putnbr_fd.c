#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	k;

	k = n;
	if (k < 0)
	{
		write(fd, "-", 1);
		k = -k;
	}
	if (k >= 0)
	{
		if (k < 10)
			write (fd, &"0123456789"[k], 1);
		else
		{
			ft_putnbr_fd(k / 10, fd);
			write(fd, &"0123456789"[k % 10], 1);
		}
	}
}
